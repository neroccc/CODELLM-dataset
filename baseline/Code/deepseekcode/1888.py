from typing import List
class Solution:
    def minFlipsMonoIncr(self, s: str) -> int:
        n = len(s)
        left_zeros = [0]*n
        right_ones = [0]*n
        ones = 0
        zeros = 0
        for i in range(n):
            if s[i] == '1':
                ones += 1
            else:
                zeros += 1
            left_zeros[i] = zeros
        for i in range(n-1, -1, -1):
            if s[i] == '1':
                ones -= 1
            else:
                zeros -= 1
            right_ones[i] = ones
        return min(left_zeros[i] + right_ones[i] for i in range(n))




















































































































































































































































































































